/*-------------------------------------------------------------------------

 リクエスト플래그コントローラ

---------------------------------------------------------------------------*/

/*-------------------------------------------------------------------------
 using
---------------------------------------------------------------------------*/

/*-------------------------------------------------------------------------
 
---------------------------------------------------------------------------*/
namespace Utility
{
	/*-------------------------------------------------------------------------
	 
	---------------------------------------------------------------------------*/
	public class RequestCtrl
	{
		private bool						m_request;
		private object						m_arg1;			// 引수1
		private object						m_arg2;			// 引수2

		/*-------------------------------------------------------------------------
		 
		---------------------------------------------------------------------------*/
		public object Arg1{				get{	return m_arg1;			}}
		public object Arg2{				get{	return m_arg2;			}}

		/*-------------------------------------------------------------------------
		 
		---------------------------------------------------------------------------*/
		public RequestCtrl()
		{
			CancelRequest();
			m_arg1		= null;
			m_arg2		= null;
		}

		/*-------------------------------------------------------------------------
		 リクエスト
		---------------------------------------------------------------------------*/
		public void Request()
		{
			m_request	= true;
			m_arg1		= null;
			m_arg2		= null;
		}
		public void Request(object arg1)
		{
			m_request	= true;
			m_arg1		= arg1;
			m_arg2		= null;
		}
		public void Request(object arg1, object arg2)
		{
			m_request	= true;
			m_arg1		= arg1;
			m_arg2		= arg2;
		}

		/*-------------------------------------------------------------------------
		 リクエスト취소
		 引수は破棄されない
		---------------------------------------------------------------------------*/
		public void CancelRequest()
		{
			m_request	= false;
		}

		/*-------------------------------------------------------------------------
		 リクエストの有無を得る
		 플래그はリクエスト없음に戻される
		---------------------------------------------------------------------------*/
		public bool IsRequest()
		{
			bool	is_req	= m_request;
			CancelRequest();
			return is_req;
		}
	}
}
