/*-------------------------------------------------------------------------

 파일に関する操作

---------------------------------------------------------------------------*/

/*-------------------------------------------------------------------------
 using
---------------------------------------------------------------------------*/
using System.IO;

/*-------------------------------------------------------------------------
 
---------------------------------------------------------------------------*/
namespace Utility
{
	/*-------------------------------------------------------------------------
	 
	---------------------------------------------------------------------------*/
	public static class file_ctrl
	{
		/*-------------------------------------------------------------------------
		 파일の삭제
		---------------------------------------------------------------------------*/
		static public bool RemoveFile(string fname)
		{
			try{
				// 파일が存在しない
				if(!File.Exists(fname))		return true;

				// 삭제する
				File.Delete(fname);
			}catch{
				return false;
			}
			return true;
		}
	
		/*-------------------------------------------------------------------------
		 フォルダの작성
		---------------------------------------------------------------------------*/
		static public bool CreatePath(string path)
		{
			try{
				DirectoryInfo	di	= new DirectoryInfo(path);
				if(!di.Exists){
					di.Create();
				}
			}catch{
				// 작성실패
				return false;
			}
			return true;
		}
	}
}
